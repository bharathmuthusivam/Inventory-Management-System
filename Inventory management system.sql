-- SUPPLIER TABLE CREATION --

CREATE TABLE SUPPLIER
(
SID CHAR(5) PRIMARY KEY,
SNAME VARCHAR(30) NOT NULL,
SADD VARCHAR(40) NOT NULL,
SCITY VARCHAR(30) DEFAULT 'DELHI',
SPHONE CHAR(15) UNIQUE,
EMAIL VARCHAR(50)
);

-- PRODUCT TABLE CREATION --

CREATE TABLE PRODUCT
(
PID CHAR(5) PRIMARY KEY,
PDESC VARCHAR(40) NOT NULL,
PRICE INT CHECK (PRICE > 0),
CATEGORY CHAR(5) CHECK(CATEGORY IN ('IT','HA','HC')),
SID CHAR(5) CONSTRAINT FK_SID REFERENCES SUPPLIER(SID)
);

-- STOCK TABLE CREATION --

CREATE TABLE STOCK
(
PID CHAR(5) CONSTRAINT FK_PIDSTK REFERENCES PRODUCT(PID),
SQTY INT CHECK(SQTY >= 0),
ROL INT CHECK(ROL > 0),
MOQ INT CHECK(MOQ >=5)
);

-- CUSTOMER TABLE CREATION --

CREATE TABLE CUST
(
CID CHAR(5) PRIMARY KEY,
CNAME VARCHAR(30) NOT NULL,
ADDRESS VARCHAR(40) NOT NULL,
CITY VARCHAR(20) NOT NULL,
PHONE CHAR(15) NOT NULL,
EMAIL VARCHAR(50) NOT NULL,
DOB DATE CHECK(DOB < '01-JAN-2000')
);

-- ORDERS TABLE CREATION --

CREATE TABLE ORDERS
(
OID CHAR(5) PRIMARY KEY,
ODATE DATE,
PID CHAR(5) CONSTRAINT FK_PID_ODR REFERENCES PRODUCT(PID),
CID CHAR(5) CONSTRAINT FK_CID REFERENCES CUST(CID),
OQTY INT CHECK(OQTY >=1)
);

