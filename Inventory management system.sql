-- UPDATE TRIGGER --
CREATE TRIGGER TR_UPD_ORD
ON ORDERS
FOR UPDATE
AS
BEGIN
		DECLARE @QS AS INT;
		DECLARE @OQ AS INT;
		DECLARE @NQ AS INT;

		SET @OQ = (SELECT OQTY FROM DELETED);
		SET @NQ = (SELECT OQTY FROM INSERTED);
		SET @QS = (SELECT SQTY FROM STOCK WHERE PID = (SELECT PID FROM INSERTED));

		IF @QS >= @NQ
			BEGIN
					UPDATE STOCK SET SQTY = @QS+@OQ - @NQ
					WHERE PID = (SELECT PID FROM INSERTED);
					COMMIT;
					PRINT('ORDER UPDATED');
			END;

		ELSE
			BEGIN
					ROLLBACK;
					PRINT('INSUFFICIENT BALANCE ORDER REJECTED');
			END;
END;

