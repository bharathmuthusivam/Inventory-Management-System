 -- ADDSUPPLIER – SHOULD ADD THE SUPPLIER IN THE SUPLIER TABLE AND DISPLAY THE DETAILS OF THE NEW SUPPLIER ADDED.

 -- dropping ADDSUPPLIER procedure -- 
 DROP PROCEDURE ADDSUPPLIER;

 SELECT * FROM SUPPLIER;
 --creating sequence for SUPPLIER
 CREATE SEQUENCE SIDSEQ
 AS INT
 START WITH 11
 INCREMENT BY 1;

 -- creating function using select case for ID generation
 CREATE FUNCTION IDFUNC (@C AS CHAR(1), @I AS INT)
 RETURNS CHAR(5)
 AS
 BEGIN
		DECLARE @ID AS CHAR(5);

		SELECT @ID = CASE
						WHEN @I < 10 THEN CONCAT(@C, '000', @I)
						WHEN @I < 100 THEN CONCAT(@C, '00', @I)
						WHEN @I < 1000 THEN CONCAT(@C, '0', @I)
						WHEN @I < 10000 THEN CONCAT(@C, @I)
						ELSE 'NA'
		END;
		RETURN @ID;
 END;

 -- creating ADDSUPPLIER procedure using IDFUNC function --
CREATE PROCEDURE ADDSUPPLIER @SN AS VARCHAR(30), @SA AS VARCHAR(40), @SC AS VARCHAR(30), @SPH AS CHAR(15), @SEM AS VARCHAR(50)
AS
BEGIN
	DECLARE @ID AS CHAR(5);
	DECLARE @I AS INT;
	
	SET @I = (NEXT VALUE FOR SIDSEQ);
	SET @ID = DBO.IDFUNC('S',@I);

	INSERT INTO SUPPLIER
	VALUES(@ID, @SN, @SA, @SC, @SPH, @SEM);

	SELECT * FROM SUPPLIER WHERE SID = @ID;
END;

